        public static IEnumerable<string> GetGlyphEnumerator(string value)
        {
            TextElementEnumerator glyphEnumerator = StringInfo.GetTextElementEnumerator(value);
            while (glyphEnumerator.MoveNext())
            {
                string glyph = glyphEnumerator.GetTextElement();

                // For long glyphs, also include the next text element.
                if (glyph == "K͟" || glyph == "k͟")
                {
                    if (!glyphEnumerator.MoveNext())
                    {
                        yield return glyph;
                        yield break;
                    }

                    glyph = glyph + glyphEnumerator.GetTextElement();
                }

                yield return glyph;
            }
        }

        public static HashSet<string> CreateGlyphSet(IEnumerable<string>[] validGlyphArrays)
        {
            HashSet<string> validGlyphs = new HashSet<string>();

            foreach (IEnumerable<string> validGlyphArray in validGlyphArrays)
            {
                validGlyphs.UnionWith(validGlyphArray);
            }

            return validGlyphs;
        }

        public static Dictionary<string, string> CreateTransliterationTable()
        {
            Dictionary<string, string> table = new Dictionary<string, string>(LatinLetters);

            foreach (string glyph in NonLettersN1)
            {
                // By default, non-letters are mapped to themselves.
                string searchForm = NonLetterTransliterations.GetValueOrDefault(glyph, glyph);
                table.Add(glyph, searchForm);
            }

            foreach (string glyph in NonLettersN2)
            {
                string searchForm = NonLetterTransliterations.GetValueOrDefault(glyph, glyph);
                table.Add(glyph, searchForm);
            }

            foreach (string glyph in NonLettersN3)
            {
                string searchForm = NonLetterTransliterations.GetValueOrDefault(glyph, glyph);
                table.Add(glyph, searchForm);
            }

            foreach (string glyph in NonLettersN4)
            {
                string searchForm = NonLetterTransliterations.GetValueOrDefault(glyph, glyph);
                table.Add(glyph, searchForm);
            }

            return table;
        }
